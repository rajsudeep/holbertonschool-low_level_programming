#include "3-calc.h"

/**
 * op_add - sum integers a and b
 *
 * @a: input integer
 * @b: input integer
 * Return: result of operation
 *
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference of integers a and b
 *
 * @a: input integer
 * @b: input integer
 * Return: result of operation
 *
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply integers a and b
 *
 * @a: input integer
 * @b: input integer
 * Return: result of operation
 *
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide integers a by b
 *
 * @a: input integer
 * @b: input integer
 * Return: result of operation
 *
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division of integers a by b
 *
 * @a: input integer
 * @b: input integer
 * Return: result of operation
 *
 */
int op_mod(int a, int b)
{
	return (a % b);
}
